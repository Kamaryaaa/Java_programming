package day04_concatenation;

public class Concatenation_vs_Addition {
    public static void main(String[] args) {

        System.out.println("32"+1);//321// works as concatenation
        System.out.println(7 +"1");//"71"  //works as concatenation
        System.out.println("13"+"2");//"132"// works as concatenation

        System.out.println("-------------------------------");

        System.out.println(12 + 2.5);//+ works as addition ==> 14.5




    }
}
